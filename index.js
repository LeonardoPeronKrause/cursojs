function max(n1, n2) {
    if (n1 > n2) {
        return console.log('O numero maior é n1')
    } else if (n1 < n2) {
        return console.log('O numero maior é n2')
    } else if (n1 === n2) {
        return console.log('Os numeros sao iguais')
    } else {
        return console.log('Numero invalido')
    }
}

max(10, 2);